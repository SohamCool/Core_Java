package Abstraction_Exercises;

public class DemoClass {
    int id;
    String name;
    long contact;
    double salary;
    boolean married;
    char block;

    public static void main(String[] args) {
        DemoClass demoClass = new DemoClass();
        System.out.println(demoClass.id);
        System.out.println(demoClass.name);
        System.out.println(demoClass.salary);
        System.out.println(demoClass.married);
        System.out.println(demoClass.contact);
        System.out.println(demoClass.block);
    }
}
